citites = ['Lahore' , 'Karachi' , 'Islamabad' , 'Multan' , 'Gujranwala']

print('Original')
print(citites)


print('Sorted')
print(sorted(citites))

print('Original')
print(citites)


print('Reverse')
print(sorted(citites , reverse=True))

print('Original')
print(citites)


print('Reversed')
citites.reverse()
print(citites)

print('Reversed Again')
citites.reverse()
print(citites)


print('Sorted')
citites.sort()
print(citites)


print('Sorted Again')
citites.sort()
print(citites)
